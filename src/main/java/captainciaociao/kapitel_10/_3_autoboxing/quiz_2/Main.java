package captainciaociao.kapitel_10._3_autoboxing.quiz_2;

public class Main {
    public static void main(String[] args) {
        Integer i11 = 1;
        Integer i12 = 1;

        System.out.println(i11 == i12);
        System.out.println(i11 <= i12);
        System.out.println(i11 >= i12);

        Integer i21 = 1000;
        Integer i22 = 1000;

        System.out.println(i21 == i22);
        System.out.println(i21 <= i22);
        System.out.println(i21 >= i22);
    }

}
